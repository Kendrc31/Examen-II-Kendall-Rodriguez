    public void mostrarTabla(){
    String titulos[] = {"Cedula", "Nombre", "Apellidos", "Genero", "Fecha Nacimiento", "Email", "Salario", "Porc Comision", "Salario Total"};
        model = new DefaultTableModel(null, titulos);

        for (int i = 0; i < lista.size(); i++) {
            empleado = lista.get(i);
            double salTotal = empleado.getSalario() + empleado.getPorcComision();
            Object nuevaFila[] = {empleado.getCedula(), empleado.getNombre(), 
                empleado.getApellidos(), empleado.getGenero(), empleado.getFechaNac(),
                empleado.getEmail(), empleado.getSalario(), empleado.getPorcComision(),salTotal };
            model.addRow(nuevaFila);

        }
        tblSalarios.setModel(model);

    }
