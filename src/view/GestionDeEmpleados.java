    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        empleado = new Empleado();

        if (!txtCedula.getText().isEmpty() && !txtNombre.getText().isEmpty()) {
            try {
                empleado.setCedula(Integer.parseInt(txtCedula.getText()));
                
                empleado.setNombre(txtNombre.getText());
                empleado.setApellidos(txtApellidos.getText());
                empleado.setGenero(Character.valueOf(txtGenero.getText()));
                empleado.setEmail(txtEmail.getText());
                empleado.setSalario(Double.parseDouble(txtSalario.getText()));
                empleado.setPorcComision(Double.parseDouble(txtPorcComision.getText()));
            

            if (lista.add(empleado)) {
                JOptionPane.showMessageDialog(this, "Pelicula insertada");
                mostrarTabla();
                btnLimpiarActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(this, "Error al intentar insertar");
            }
                
                
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "La cedula debe ser num√©rica");
                txtCedula.requestFocus();
            }
       

        } else {
            JOptionPane.showMessageDialog(this, "Se requiere la Cedula o el nombre");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblEmpleados.getSelectedRowCount() == 1) {

            int fila = tblEmpleados.getSelectedRow();
            empleado = new Empleado();

            if (!txtNombre.getText().isEmpty()) {

                empleado.setCedula(Integer.parseInt(tblEmpleados.getValueAt(fila, 0).toString()));

                empleado.setNombre(txtNombre.getText());
                empleado.setApellidos(txtApellidos.getText());
                empleado.setGenero(Character.valueOf(txtGenero.getText()));

                if (lista.set(WIDTH, empleado)!= null) {
                    JOptionPane.showMessageDialog(this, "Empleado actualizado");
                    mostrarTabla();
                    btnLimpiarActionPerformed(null);
                } else {
                    JOptionPane.showMessageDialog(this, "Error al editar");
                }

            }

        } else {
            JOptionPane.showMessageDialog(this, "Se debe seleccionar 1 registro");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tblEmpleados.getSelectedRowCount() == 1) {
            int resp = JOptionPane.showConfirmDialog(this, "¬øDesea borrar el resgitro?");
            if (resp == 0) {  //El usuario quiere eliminar, Respuesta si
                int fila = tblEmpleados.getSelectedRow();
                if (lista.remove(lista.get(fila))) {
                    mostrarTabla();

                } else {
                    JOptionPane.showMessageDialog(this, "Error al intentar Borrar");
                }

            }
        } else {
            JOptionPane.showMessageDialog(this, "Se debe seleccionar al menos 1 registro");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    public void mostrarTabla(){
    String titulos[] = {"Cedula", "Nombre", "Apellidos", "Genero", "Fecha Nacimiento", "Email", "Salario", "Porc Comision"};
        model = new DefaultTableModel(null, titulos);

        for (int i = 0; i < lista.size(); i++) {
            empleado = lista.get(i);
            Object nuevaFila[] = {empleado.getCedula(), empleado.getNombre(), 
                empleado.getApellidos(), empleado.getGenero(), empleado.getFechaNac(),
                empleado.getEmail(), empleado.getSalario(), empleado.getPorcComision()};
            model.addRow(nuevaFila);

        }
        tblEmpleados.setModel(model);

        txtCant.setText(String.valueOf(model.getRowCount()));
    }
